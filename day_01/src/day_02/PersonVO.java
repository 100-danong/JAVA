package day_02;

public class PersonVO {
	
	public PersonVO() {
	}
	
	
	public PersonVO(String name, int age, int height, String gender) {
		super();
		this.name = name;
		this.age = age;
		this.height = height;
		this.gender = gender;
	}
	
	
	String name;
	int age;
	int height;
	String gender;
	

}
